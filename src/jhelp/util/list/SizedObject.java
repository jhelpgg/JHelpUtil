package jhelp.util.list;

/**
 * Indactes that the object have width and height
 * 
 * @author JHelp
 */
public interface SizedObject
{
   /**
    * Object height
    * 
    * @return Object height
    */
   public int getHeight();

   /**
    * Object width
    * 
    * @return Object width
    */
   public int getWidth();
}